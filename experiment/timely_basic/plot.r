library(gridExtra)
library(ggplot2)

tableBasic1 <- read.table("test1.dat", header=TRUE, sep=",")
frameBasic1 <- as.data.frame(tableBasic1)
plotBasic1 <- ggplot(frameBasic1, aes(x=Time/1000/1000, y=Rate/1000/1000/1000)) + geom_point()
plotBasic1 = plotBasic1 + labs(x="Time (sec)")
plotBasic1 = plotBasic1 + labs(y="TX Rate (GB/sec)")
plotBasic1 = plotBasic1 + labs(title="Timely Simulated Rate Computation")
plotBasic1 = plotBasic1 + labs(subtitle="NIC Bandwidth 10 GB/sec, alpha=0.875, beta=0.8, delta=10MB/sec, modelRttMin=50us, modelRttMax=500us")
plotBasic1 = plotBasic1 + labs(caption="RTTs sampled from Normal Dist with mean=225us, stddev=4")

